function simulateColoring(N, X, A) {
    const balls = A.split('');
    const queue = [];

    // 0-indexed „Å´Â§âÊèõ
    const start = X - 1;
    balls[start] = '@';
    queue.push(start);

    while (queue.length > 0) {
        const pos = queue.shift();

        // Â∑¶Èö£
        const left = pos - 1;
        if (left >= 0 && balls[left] === '.') {
            balls[left] = '@';
            queue.push(left);
        }

        // Âè≥Èö£
        const right = pos + 1;
        if (right < N && balls[right] === '.') {
            balls[right] = '@';
            queue.push(right);
        }
    }

    return balls.join('');
}

